/* USER CODE BEGIN 4 */
void Read_ADC_Values(void) {
    // Start the conversion process
    HAL_ADC_Start(&hadc);
    //HAL_ADC_Start(&hadc);

    // Read each channel's ADC value
    for (uint8_t i = 0; i < ADC_BUFFER_SIZE; i++) {
        // Wait for the end of conversion and read the ADC value
        if (HAL_ADC_PollForConversion(&hadc, 1000) == HAL_OK) {
            adc_values[i] = HAL_ADC_GetValue(&hadc);
        }
    }

    // Stop ADC conversion
    HAL_ADC_Stop(&hadc);

    // Print ADC values via UART
    snprintf(uart_buffer, sizeof(uart_buffer), "ADC Values: PA1=%lu, PA4=%lu, PA5=%lu, PC4=%lu, PC3=%lu\r\n",
             adc_values[0], adc_values[1], adc_values[2], adc_values[3], adc_values[4]);
    HAL_UART_Transmit(&huart3, (uint8_t*)uart_buffer, strlen(uart_buffer), HAL_MAX_DELAY);
}

void Calculate_Speed_Level(void) {
    // Assuming adc_values[4] corresponds to the PAS Cadence sensor
    uint32_t cadence_adc_value = adc_values[4];
    uint32_t speed_level = (cadence_adc_value * 100) / ADC_MAX_VALUE; // Scale to 0-100%

    // Print the speed level via UART
    snprintf(uart_buffer, sizeof(uart_buffer), "Speed Level: %lu%%\r\n", speed_level);
    HAL_UART_Transmit(&huart3, (uint8_t*)uart_buffer, strlen(uart_buffer), HAL_MAX_DELAY);
}
